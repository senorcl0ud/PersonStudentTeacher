package personstudentteacher;

public class Person {
    private final long ID;
    private String name;




    public Person (long ID, String name) {
        this.id = ID;
        this.name = name;
    }

    public long getId() {
        return id;
    }


    public String getName(){
        return name;
    }

    public void setName() {
        this.setName();
    }

}
